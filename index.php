        function initTailorsSlider() {
            if (typeof jQuery === 'undefined' || typeof jQuery.fn.slick === 'undefined') {
                return;
            }

            const applySliderConfig = () => {
                const slider = jQuery('.tailors-slider');
                if (!slider.length) {
                    return;
                }

                if (window.innerWidth < 1200) {
                    if (!slider.hasClass('slick-initialized')) {
                        slider.slick({
                            dots: true,
                            infinite: true,
                            speed: 500,
                            slidesToShow: 4,
                            slidesToScroll: 1,
                            autoplay: true,
                            autoplaySpeed: 3000,
                            pauseOnHover: true,
                            arrows: true,
                            responsive: [
                                {
                                    breakpoint: 1200,
                                    settings: {
                                        slidesToShow: 3,
                                        slidesToScroll: 1
                                    }
                                },
                                {
                                    breakpoint: 992,
                                    settings: {
                                        slidesToShow: 2,
                                        slidesToScroll: 1
                                    }
                                },
                                {
                                    breakpoint: 768,
                                    settings: {
                                        slidesToShow: 1,
                                        slidesToScroll: 1,
                                        arrows: false
                                    }
                                }
                            ]
                        });
                    }
                } else if (slider.hasClass('slick-initialized')) {
                    slider.slick('unslick');
                }
            };

            applySliderConfig();

            if (!window._tailorsSliderResizeBound) {
                let resizeTimeout;
                window.addEventListener('resize', () => {
                    clearTimeout(resizeTimeout);
                    resizeTimeout = setTimeout(applySliderConfig, 200);
                });
                window._tailorsSliderResizeBound = true;
            }
        }

        function generateStars(rating) {
            const fullStars = Math.floor(rating);
            const halfStar = rating % 1 >= 0.5;
            const emptyStars = 5 - fullStars - (halfStar ? 1 : 0);
            
            let stars = '';
            for (let i = 0; i < fullStars; i++) {
                stars += '<i class="fas fa-star"></i>';
            }
            if (halfStar) {
                stars += '<i class="fas fa-star-half-alt"></i>';
            }
            for (let i = 0; i < emptyStars; i++) {
                stars += '<i class="far fa-star"></i>';
            }
            
            return stars;
        }
        
